#!/usr/bin/env zx
import { mkdir, readFile, writeFile, chmod } from "node:fs/promises";

const OPENCLAW_DIR = "/root/.openclaw";
const WORKSPACE_DIR = `${OPENCLAW_DIR}/workspace`;
const NVMRC_PATH = `${OPENCLAW_DIR}/.nvmrc`;
const PM2_CONFIG_PATH = `${OPENCLAW_DIR}/pm2.config.js`;
const TEMPLATE_PATH = "/root/openclaw.json.template";
const OUTPUT_PATH = `${OPENCLAW_DIR}/openclaw.json`;

const telegramBotToken = process.env.TELEGRAM_BOT_TOKEN ?? "";

await mkdir(WORKSPACE_DIR, { recursive: true });

// Pin Node 24 for shells that `cd` here (nvm + .nvmrc integration).
await writeFile(NVMRC_PATH, "24\n", "utf8");

const template = await readFile(TEMPLATE_PATH, "utf8");
let openclawJson = template.replaceAll("${TELEGRAM_BOT_TOKEN}", telegramBotToken);

const extraOriginRaw = process.env.OPENCLAW_ALLOWED_ORIGIN;
if (extraOriginRaw != null && String(extraOriginRaw).trim() !== "") {
  const extra = String(extraOriginRaw)
    .split(",")
    .map((s) => s.trim())
    .filter(Boolean);
  const cfg = JSON.parse(openclawJson);
  cfg.gateway ??= {};
  cfg.gateway.controlUi ??= {};
  const existing = cfg.gateway.controlUi.allowedOrigins;
  const merged = Array.isArray(existing) ? [...existing] : [];
  for (const origin of extra) {
    if (!merged.includes(origin)) merged.push(origin);
  }
  cfg.gateway.controlUi.allowedOrigins = merged;
  openclawJson = `${JSON.stringify(cfg, null, 2)}\n`;
}

await writeFile(OUTPUT_PATH, openclawJson, {
  encoding: "utf8",
  mode: 0o600,
});

try {
  await chmod(OUTPUT_PATH, 0o600);
} catch {
  // Ignore chmod failures on environments that do not support changing mode.
}

// PM2 is started after `nvm use 24` (see supervisord); `openclaw` resolves from PATH on Node 24.
const pm2Config = `// Generated by bootstrap-openclaw.mjs — use with: nvm use 24 && pm2 start pm2.config.js
module.exports = {
  apps: [
    {
      name: "openclaw-gateway",
      script: "openclaw gateway"
    },
  ],
};
`;

await writeFile(PM2_CONFIG_PATH, pm2Config, { encoding: "utf8", mode: 0o600 });

try {
  await chmod(PM2_CONFIG_PATH, 0o600);
} catch {
  // Ignore chmod failures on environments that do not support changing mode.
}
